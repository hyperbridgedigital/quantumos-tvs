const DEFAULT_LOCALE = 'en';

export function getLocale() {
  if (typeof window === 'undefined') return DEFAULT_LOCALE;
  try {
    return localStorage.getItem('qos_locale') || DEFAULT_LOCALE;
  } catch {
    return DEFAULT_LOCALE;
  }
}

export function setLocale(code) {
  try {
    if (typeof window !== 'undefined') localStorage.setItem('qos_locale', code);
  } catch {}
}

export function t(key, locale = getLocale()) {
  const map = translations[locale] || translations[DEFAULT_LOCALE];
  return map?.[key] ?? translations[DEFAULT_LOCALE]?.[key] ?? key;
}
